USE churn_raw;

-- ========================================================================================================================================================================================================
-- churn_events →	documents churn outcomes for accounts that ended their service
-- --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
CREATE TABLE churn_raw.churn_events (
    churn_event_id           VARCHAR(255),
    account_id               VARCHAR(255),
    churn_date               VARCHAR(255),
    reason_code              VARCHAR(255),
    refund_amount_usd        VARCHAR(255),
    preceding_upgrade_flag   VARCHAR(255),
    preceding_downgrade_flag VARCHAR(255),
    is_reactivation          VARCHAR(255),
    feedback_text            VARCHAR(255)
);